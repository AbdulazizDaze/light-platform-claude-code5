import { beforeEach, describe, expect, it, vi } from "vitest";
import { FakeFirestore, SERVER_TIMESTAMP_SENTINEL } from "@/lib/firebase/test-fakes/fake-admin-firestore";
import type { CandidateRegistration } from "@/lib/schemas/user";

// `registerCandidate` imports `FieldValue` from `firebase-admin/firestore` at
// module scope purely for `FieldValue.serverTimestamp()` sentinels — stub it
// so this test never initializes a real Admin SDK connection.
vi.mock("firebase-admin/firestore", () => ({
  FieldValue: {
    serverTimestamp: () => SERVER_TIMESTAMP_SENTINEL,
  },
}));

const { registerCandidate } = await import("./register-candidate");

function makeInput(overrides: Partial<CandidateRegistration> = {}): CandidateRegistration {
  return {
    name: "أحمد",
    phone: "+966500000000",
    city: "Riyadh",
    gender: "male",
    nationality: "saudi",
    lang_pref: "ar",
    consent_accepted: true,
    ...overrides,
  } as CandidateRegistration;
}

describe("registerCandidate", () => {
  let db: FakeFirestore;

  beforeEach(() => {
    db = new FakeFirestore();
  });

  it("creates users/{uid} and an empty-shell candidate_profiles/{uid} on first registration", async () => {
    const result = await registerCandidate(db as never, "uid-1", makeInput());

    expect(result.created).toBe(true);

    const user = db.raw("users/uid-1") as Record<string, unknown>;
    expect(user.uid).toBe("uid-1");
    expect(user.role).toBe("candidate");
    expect(user.name).toBe("أحمد");
    expect(user.created_at).toEqual(SERVER_TIMESTAMP_SENTINEL);
    expect(user.consent_accepted_at).toEqual(SERVER_TIMESTAMP_SENTINEL);

    const profile = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    expect(profile.profile_completeness).toBe(0);
    expect(profile.cv_template).toBe("classic");
    expect(profile.education).toEqual([]);
    expect((profile.personal as Record<string, unknown>).name).toBe("أحمد");
  });

  it("sets role to candidate regardless of any attempt elsewhere to override it", async () => {
    // CandidateRegistrationSchema is `.strict()` and has no `role` field, so
    // there's no way to smuggle one in via `input` — this asserts the
    // function's OWN behavior never reads a role from anywhere but its
    // hardcoded constant.
    const result = await registerCandidate(db as never, "uid-1", makeInput());
    expect(result.created).toBe(true);
    const user = db.raw("users/uid-1") as Record<string, unknown>;
    expect(user.role).toBe("candidate");
  });

  it("omits email from users and profile.personal when not provided", async () => {
    await registerCandidate(db as never, "uid-1", makeInput());
    const user = db.raw("users/uid-1") as Record<string, unknown>;
    const profile = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    expect(user).not.toHaveProperty("email");
    expect(profile.personal).not.toHaveProperty("email");
  });

  it("includes email when provided", async () => {
    await registerCandidate(db as never, "uid-1", makeInput({ email: "a@example.com" }));
    const user = db.raw("users/uid-1") as Record<string, unknown>;
    expect(user.email).toBe("a@example.com");
  });

  it("on re-registration, updates name/phone/city but preserves uid, role, created_at, consent_accepted_at", async () => {
    await registerCandidate(db as never, "uid-1", makeInput({ name: "أحمد" }));
    const firstUser = db.raw("users/uid-1") as Record<string, unknown>;

    const result = await registerCandidate(
      db as never,
      "uid-1",
      makeInput({ name: "محمد", city: "Jeddah" }),
    );

    expect(result.created).toBe(false);
    const secondUser = db.raw("users/uid-1") as Record<string, unknown>;
    expect(secondUser.name).toBe("محمد");
    expect(secondUser.city).toBe("Jeddah");
    // Identity/consent fields untouched by the second call.
    expect(secondUser.uid).toBe(firstUser.uid);
    expect(secondUser.role).toBe(firstUser.role);
    expect(secondUser.created_at).toEqual(firstUser.created_at);
    expect(secondUser.consent_accepted_at).toEqual(firstUser.consent_accepted_at);
  });

  it("on re-registration, merges into the existing profile.personal without touching CV content fields", async () => {
    await registerCandidate(db as never, "uid-1", makeInput());
    // Simulate CV content already generated by a prior chat session.
    const profileBefore = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    db.seed("candidate_profiles/uid-1", {
      ...profileBefore,
      professional_summary: { en: "Existing summary.", ar: "ملخص موجود." },
      skills: [{ name: "Sales", level: 3, category: "soft", inferred: true }],
    });

    await registerCandidate(db as never, "uid-1", makeInput({ name: "محمد" }));

    const profileAfter = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    expect((profileAfter.personal as Record<string, unknown>).name).toBe("محمد");
    // CV content untouched by re-registration.
    expect(profileAfter.professional_summary).toEqual({ en: "Existing summary.", ar: "ملخص موجود." });
    expect(profileAfter.skills).toEqual([{ name: "Sales", level: 3, category: "soft", inferred: true }]);
  });

  it("returns created: false and does not reset profile_completeness on re-registration", async () => {
    await registerCandidate(db as never, "uid-1", makeInput());
    const profileBefore = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    db.seed("candidate_profiles/uid-1", { ...profileBefore, profile_completeness: 55 });

    const result = await registerCandidate(db as never, "uid-1", makeInput());

    expect(result.created).toBe(false);
    const profileAfter = db.raw("candidate_profiles/uid-1") as Record<string, unknown>;
    expect(profileAfter.profile_completeness).toBe(55);
  });
});
